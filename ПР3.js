function sumSequence(n) {
    if (n >= 1) {
        return (n * (n + 1)) / 2; // Формула для обчислення суми послідовності чисел від 1 до n
    } else {
        return (n * (n - 1)) / 2; // Формула для обчислення суми послідовності чисел від n до 1 при n < 0
    }
}